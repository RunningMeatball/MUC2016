task main(){
	startTask(checkCollision);
	int index;
	int length = sizeof(arr) / 2; //divide by 2 for int, divide by 20 for string
	for(index = 0; index < length - 1; index++){
		int a = arr[index];
		switch(a){
			case 1:
				turn('l', false);
				break;
			case 2:
				turn('r', false);
				break;
			case 3:
				moveForward(true, false);
				break;
			case 4:
				turnOntoCircular();
				break;
			case 5:
				circularForward();
				break;
			case 6:
				specialCircular();
				break;
		}
	}
	// Last turn and then park
	int b = arr[length - 1];
	switch(b){
		case 1:
			turn('l', true);
			break;
		case 2:
			turn('r', true);
			break;
		case 3:
			moveForward(true, true);
			break;
	}
	// parking
	parking();
}

