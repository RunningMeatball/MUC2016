task checkCollision(){
	while(true){
		wait1Msec(10);
		if(motor[motorL] == 0 && motor[motorR] == 0){
			continue;
		}

		if(SensorValue[sensorD] < 10){
			suspendTask(main);
			int l = motor[motorL];
			int r = motor[motorR];
			motor[motorL] = 0;
			motor[motorR] = 0;
			wait1Msec(2000);
			motor[motorL] = l;
			motor[motorR] = r;
			startTask(main);
		}
	}
}

